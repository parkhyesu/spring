package edu.bit.ex;

public class Student {

	//String 은 초기화 될때 기본값으로 null
	private String name;
	//int 는 초기화 될때 기본값으로 0
	private int id;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
